/**
 * Helper Functions
 */
export function getDocumentById(id: string): DocumentAsset | undefined {
  return documentAssets.find((doc) => doc.id === id);
}

export function getDocumentsByProductSlug(productSlug: string): DocumentAsset[] {
  return documentAssets.filter((doc) => doc.productSlug === productSlug);
}

export function getDocumentsByLotNo(lotNo: string): DocumentAsset[] {
  return documentAssets.filter((doc) => doc.lotNo === lotNo);
}

export function getDocumentsByType(type: string): DocumentAsset[] {
  return documentAssets.filter((doc) => doc.type === type);
}

export function getDocumentsByLang(lang: "zh" | "en" | "ja"): DocumentAsset[] {
  return documentAssets.filter((doc) => doc.lang === lang);
}

// Export alias for compatibility
export const documents = documentAssets;
