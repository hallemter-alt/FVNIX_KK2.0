/**
 * Helper Functions
 */
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getProductsBySeries(series: string): Product[] {
  return products.filter(p => p.series === series);
}

export function getProductsByTag(tag: string): Product[] {
  return products.filter(p => p.tags?.includes(tag));
}

export function getAllSeries(): string[] {
  const seriesSet = new Set(products.map(p => p.series).filter(Boolean) as string[]);
  return Array.from(seriesSet).sort();
}

export function getAllTags(): string[] {
  const tagsSet = new Set(products.flatMap(p => p.tags || []));
  return Array.from(tagsSet).sort();
}
